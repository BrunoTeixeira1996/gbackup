package utils

import (
	"fmt"
	"log"
	"time"
)

func Header() {
	currentDate := time.Now().Format("2006/01/02 15:04:05")

	fmt.Printf("==========================================================================\n")
	log.Println("Start Gbackup:", currentDate)
	fmt.Printf("==========================================================================\n")
}

func Body(txt string) {
	currentDate := time.Now().Format("2006/01/02 15:04:05")

	fmt.Printf("==========================================================================\n")
	log.Printf("%s: %s\n", txt, currentDate)
	fmt.Printf("==========================================================================\n")
}

func Footer() {
	currentDate := time.Now().Format("2006/01/02 15:04:05")

	fmt.Printf("==========================================================================\n")
	log.Println("End Gbackup:", currentDate)
	fmt.Printf("==========================================================================\n\n\n")
	fmt.Printf("------------------------------------------------------------------------------------------------------------------------------------------------------------\n\n\n")

}
